def getIntlMaps():
    baseURL = 'http://www.cpc.noaa.gov/products/analysis_monitoring/regional_monitoring/'
    products = ['pcp', 'max', 'min', 'tan']
    times = ['w', '1', '3']

    allLinks = []
    for rgn in range(22):  # Loop through all 22 available regions
        region = []
        for timeScale in times:
            time = []
            for product in products:
                link = baseURL + timeScale + 'c' + product + str(rgn) + '.png'
                time.append(link)
            region.append(time)
        allLinks.append(region)

    return allLinks
